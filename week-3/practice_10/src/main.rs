fn main() {
    // addition
    let sum = 5550 + 7310;
    println!("The sum of 5550 and 7310 = {}", sum);

    // subtraction
    let difference:u32 = 95 - 4;
    println!("The difference of 95 and 4 = {}", difference);

    //multiplication
    let product:f32 = 4.0 * 30.0;
    println!("The multiplication of 4.0 and 30.0 = {}", product);

    //division
    let quotient = 56.7 / 32.2;
    println!("The division of 56.7 and 32.2 = {}", quotient);

    //remainder
    let remainder = 43 % 5;
    println!("The remainder of 43 and 5 = {}", remainder);
}
